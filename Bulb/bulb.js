// Bulb on js
function bulbOn(){
    let bulbOnOff = document.querySelector('.bulbOff');
    bulbOnOff.setAttribute("src", '/Bulb/img/pinkBulb-off.jpg')
}
function bulbOff(){
    let bulbOnOff = document.querySelector('.bulbOff');
    bulbOnOff.setAttribute("src", '/Bulb/img/pinkBulb-on.jpg');
    
}